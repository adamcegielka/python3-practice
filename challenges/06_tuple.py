# # Tuple Examples

numbers = (50, 100, 150, 200, 250)
print(f"Numbers: {numbers}")  # Numbers: (50, 100, 150, 200, 250)
print(type(numbers))  # <class 'tuple'>
print(f"Tuple length: {len(numbers)}")  # 5
print(f"Last item: {numbers[-1]}") # Last item: 250
print(f"Slicing (1:4): {numbers[1:4]}") # Slicing (1:4): (100, 150, 200)
print(f"Every second item: {numbers[::2]}")  # Every second item\
print(f"Third item to end: {numbers[-3]}")  # Third item to end: 150]}")
