number1 = 10
number2 = 20

print("Check if number is positive:", number1 > 0)   # True
print("Check if number is negative:", number2 < 0)   # False
print("Check if number is equal to 0", number1 == 0)  # False
print("Check if numbers are equal:", number1 == number2)  # False

if number1 < number2:
    print(f"{number1} is less than {number2}")
else:
    print(f"{number1} is not less than {number2}")

