# Check if a number is even or odd
number = 3
if number % 2 == 0:
    print(f"{number} is an even number")
else:
    print(f"{number} is an odd number")

# Change the value of number and check again
number = 10
if number % 2 == 0:
    print(f"{number} is an even number")
else:
    print(f"{number} is an odd number")

print(number) # 10
