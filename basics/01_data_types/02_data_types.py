# Data Types

info = 14
print(type(info))  # Output: <class 'int'>

info = "Hello, Python!"
print(type(info))  # Output: <class 'str'>

info = 3.14
print(type(info))  # Output: <class 'float'>

info = True
print(type(info))  # Output: <class 'bool'>

info = [1, 2, 3, 4, 5]
print(type(info))  # Output: <class 'list'>

info = (1, 2, 3)
print(type(info))  # Output: <class 'tuple'>

info = { "name": "Alice", "age": 30 }
print(type(info))  # Output: <class 'dict'>

info = {1, 2, 3}
print(type(info))  # Output: <class 'set'>

info = None
print(type(info))  # Output: <class 'NoneType'>
